     public void displaymessage()
     {
         
         if(score==100)
         {
             jLabel5.setText("Perfect Buddy on first attempts!!!");
         }
         else if(score>=50 && score<=90)
         {
             jLabel5.setText("Well Played Buddy!!!");
             
         }
         else if(score>=10 && score<=40)
         {
             jLabel5.setText("Nice Played Buddy!!!");
             
         }
         else
         {
             jLabel5.setText("Better Luck Next Time!!!");
         }
     }

